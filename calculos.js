// exercicios de operações de calculos

const num1 = 3905;
const num2 = 2004;

// soma:

let soma = num1 + num2;
console.log("Resultado da soma: " + soma);

// subtração:

let sub = num1 - num2;
console.log("Resultado da subtração: " + sub);

// multiplicação:

let multi = num1 * num2;
console.log("Resultado da multiplicação: " + multi);

// divisão

let div = num1 / num2;
console.log("Resultado da divisão: " + div);

// numero par ou impar

if(num1 % 2 !== 0){
    console.log("O número 1 é impar")
}else{
    console.log("O número 1 é par")
}

if(num2 % 2 !== 0){
    console.log("O número 2 é impar")
}else{
    console.log("O número 2 é par")
}